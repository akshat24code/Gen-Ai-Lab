"""# Experiment 1: Generate random samples from various probability distributions (e.g., normal distribution, uniform distribution, Binomial Distribution, Poisson Distribution) using Python.

"""

import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

# Set a random seed for reproducibility
np.random.seed(42)

"""## 1. Normal Distribution (Gaussian Distribution)

The normal distribution is a symmetric, bell-shaped distribution characterized by its mean (mu) and standard deviation (sigma). Many natural phenomena follow a normal distribution.
"""

# Parameters for Normal Distribution
mu = 0  # Mean
sigma = 1 # Standard deviation
samples_normal = np.random.normal(loc=mu, scale=sigma, size=1000)

plt.figure(figsize=(8, 5))
sns.histplot(samples_normal, bins=30, kde=True, color='skyblue')
plt.title('Random Samples from Normal Distribution (Mean=0, Std Dev=1)')
plt.xlabel('Value')
plt.ylabel('Frequency')
plt.grid(True, linestyle='--', alpha=0.6)
plt.show()

"""## 2. Uniform Distribution

The uniform distribution describes an experiment where all outcomes are equally likely. It can be continuous or discrete. Here, we'll demonstrate a continuous uniform distribution.
"""

# Parameters for Uniform Distribution
low = 0   # Lower bound
high = 1  # Upper bound
samples_uniform = np.random.uniform(low=low, high=high, size=1000)

plt.figure(figsize=(8, 5))
sns.histplot(samples_uniform, bins=30, kde=True, color='lightcoral')
plt.title('Random Samples from Uniform Distribution (Low=0, High=1)')
plt.xlabel('Value')
plt.ylabel('Frequency')
plt.grid(True, linestyle='--', alpha=0.6)
plt.show()

"""## 3. Binomial Distribution

The binomial distribution models the number of successes in a fixed number of independent Bernoulli trials (experiments with only two outcomes, like coin flips).
"""

# Parameters for Binomial Distribution
n = 10    # Number of trials
p = 0.5   # Probability of success on each trial
samples_binomial = np.random.binomial(n=n, p=p, size=1000)

plt.figure(figsize=(8, 5))
sns.histplot(samples_binomial, bins=range(n + 2), discrete=True, color='lightgreen')
plt.title('Random Samples from Binomial Distribution (n=10, p=0.5)')
plt.xlabel('Number of Successes')
plt.ylabel('Frequency')
plt.xticks(range(n + 1))
plt.grid(True, linestyle='--', alpha=0.6)
plt.show()

"""## 4. Poisson Distribution

The Poisson distribution expresses the probability of a given number of events occurring in a fixed interval of time or space if these events occur with a known constant mean rate and independently of the time since the last event.
"""

# Parameters for Poisson Distribution
lambda_param = 3 # Average number of events in an interval
samples_poisson = np.random.poisson(lam=lambda_param, size=1000)

plt.figure(figsize=(8, 5))
sns.histplot(samples_poisson, bins=range(max(samples_poisson) + 2), discrete=True, color='plum')
plt.title('Random Samples from Poisson Distribution (Lambda=3)')
plt.xlabel('Number of Events')
plt.ylabel('Frequency')
plt.xticks(range(max(samples_poisson) + 1))
plt.grid(True, linestyle='--', alpha=0.6)
plt.show()